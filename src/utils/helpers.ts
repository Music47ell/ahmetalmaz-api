const getCountryName = (code2: string) => {
  if (!code2) return 'Unknown';
  code2 = code2.toUpperCase();

  // Find the country where alpha2 matches
  const entry = Object.values(COUNTRY_MAP).find(c => c.alpha2 === code2);
  return entry ? entry.name : code2; // fallback to code if not found
};

